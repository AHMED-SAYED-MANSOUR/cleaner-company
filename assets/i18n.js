(function () {
  const params = new URLSearchParams(location.search);
  const stored = localStorage.getItem("fc-lang");
  const lang = params.get("lang") || stored || "ar";
  window.FC.lang = lang;
})();

window.FC.t = function (path) {
  const dict = window.FC.i18n[window.FC.lang] || window.FC.i18n.ar;
  return path.split(".").reduce((o, k) => (o ? o[k] : undefined), dict);
};

window.FC.setLang = function (lang) {
  localStorage.setItem("fc-lang", lang);
  const url = new URL(location.href);
  url.searchParams.set("lang", lang);
  location.href = url.toString();
};

window.FC.applyLang = function () {
  const lang = window.FC.lang;
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
};

window.FC.imgUrl = function (rel) {
  return window.FC.imgBase + "/" + rel;
};
